    public void preencherTabela(String produtoVendido) {
        conectaDAO cdao = new conectaDAO();
        ProdutosDAO pdao = new ProdutosDAO();
        
        boolean sts = cdao.getConnection() != null;

        if (!sts) {
            JOptionPane.showMessageDialog(null, "Erro de conex√£o.");
        } else {

            ArrayList<ProdutosDTO> listaProdutos = pdao.listarProdutosVendidos(produtoVendido);

            DefaultTableModel tabelaVendas = (DefaultTableModel) tblVendas.getModel();

            tblVendas.setRowSorter(new TableRowSorter(tabelaVendas));

            tabelaVendas.setNumRows(0);

            for (ProdutosDTO produto : listaProdutos) {
                Object[] obj = new Object[]{
                    produto.getId(),
                    produto.getNome(),
                    produto.getStatus(),
                    produto.getValor()
                };
                tabelaVendas.addRow(obj);
            }

            cdao.desconectar();
        }
    }
